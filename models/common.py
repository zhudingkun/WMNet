class Add2(nn.Module):
    def __init__(self, c1, index, addscale, learnable=False):
        super().__init__()
        self.index = index
        self.learnable=learnable
        self.div=4
        if addscale>1:
            self.div=(1+addscale)**2
        if learnable:
            self.addscale=nn.Parameter(torch.zeros(*[1]) + addscale-1)
            self.addscale.data = self.addscale.data.clamp(min=addscale-1-0.2, max=addscale-1+0.1)
        else:
            self.addscale=addscale
    def forward(self, x):
        if self.index == 0:
            if self.learnable:
                return (torch.add((1-self.addscale[0])*x[0], (1+self.addscale[0])*x[1][0]))/self.div
            else:
                return (torch.add((2-self.addscale)*x[0], self.addscale*x[1][0]))/4
        elif self.index == 1:
            if self.learnable:
                return (torch.add((1-self.addscale[0])*x[0], (1+self.addscale[0])*x[1][1]))/self.div
            else:
                return (torch.add((2-self.addscale)*x[0], self.addscale*x[1][1]))/4
        

