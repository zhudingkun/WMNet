class WAttention(nn.Module):
    def __init__(self, dim, num_heads=3, attn_ratio=0.5):
        super().__init__()
        self.num_heads = num_heads
        self.head_dim = dim // num_heads
        self.key_dim = int(self.head_dim * attn_ratio)
        self.scale1 = 1 ** -0.5  # self.key_dim
        self.scale2 = 3 ** -0.5 
        nh_kd = self.key_dim * num_heads
        h = dim + nh_kd * 2
        self.qkv = Conv(6, 12, 1, act=False)
        self.qkv2 = Conv(18, 36, 1, act=False)
        self.ln = nn.LayerNorm(dim * 2)
        
        self.conv = nn.Conv2d(dim * 2, dim * 2,kernel_size=3,stride=2,padding=1)
        self.pe = nn.Linear(dim * 2, dim)
        self.scalem = _ScaleModule([1,1,1,1], init_scale=1.0)
        
        self.wt_filter, self.iwt_filter = create_wavelet_filter('db1', dim, dim, torch.float)
        self.iwt_filter = nn.Parameter(self.iwt_filter, requires_grad=False)
        self.iwt_function = partial(inverse_wavelet_transform, filters=self.iwt_filter)       


    def forward(self, x, x2):

        B, C, _, H, W = x.shape
        N = H * W

        x_ll = x[:, :, 0, :, :]
        x2_ll = x2[:, :, 0, :, :]
        x_ll = torch.cat([x_ll, x2_ll], dim=1)

        x_h = x[:, :, 1:4, :, :].reshape(B, C * 3, H, W)
        x2_h = x2[:, :, 1:4, :, :].reshape(B, C * 3, H, W)
        x_h = torch.cat([x_h, x2_h], dim=1)
        
        qkv = self.qkv(x_ll)
        qkv2 = self.qkv2(x_h)
        q, k, v = qkv.view(B, self.num_heads, 1 * 2 + 2, N).split(
            [1, 1, 2], dim=2)
        q2, k2, v2 = qkv2.view(B, self.num_heads, 3 * 2 + 6, N).split(
            [3, 3, 6], dim=2)
        
        attn = (q.transpose(-2, -1) @ k) * self.scale1
        attn = attn.softmax(dim=-1)

        attnh = (q2.transpose(-2, -1) @ k2) * self.scale2
        attnh = attnh.softmax(dim=-1)
        
        attnh = attn * attnh
        attn = torch.add(attn, self.scalem(attnh))
        
        t = (v @ attn.transpose(-2, -1)).reshape(B, C * 2, N)
        
        x = (t + v.reshape(B, C * 2, N)).permute(0, 2, 1)
        
        x = self.ln(x)
        x = self.pe(x).permute(0, 2, 1).view(B, C, H, W)
        
        return x


class IEB(nn.Module):
    def __init__(self, d, kernel_size=3, stride=1, bias=True, wt_type='db1'):
        super().__init__()
        kernel_size = 3
        self.in_channels = d
        self.stride = stride
        self.dilation = 1

        self.block = WAttention(3)

        self.wt_filter, self.iwt_filter = create_wavelet_filter(wt_type, d, d, torch.float)
        self.wt_filter = nn.Parameter(self.wt_filter, requires_grad=False)
        self.iwt_filter = nn.Parameter(self.iwt_filter, requires_grad=False)

        self.wt_function = partial(wavelet_transform, filters=self.wt_filter)
        self.iwt_function = partial(inverse_wavelet_transform, filters=self.iwt_filter)

        self.base_conv = nn.Conv2d(d, d, kernel_size, padding='same', stride=1, dilation=1,
                                   groups=d, bias=bias)
        self.base_scale = _ScaleModule([1, d, 1, 1])
        self.scale = _ScaleModule([1, d, 1, 1], init_scale=0.5)

        #self.wavelet_convs = nn.Conv2d(d * 4, d * 4, kernel_size, padding='same', stride=1, dilation=1, groups=d * 4, bias=False)
        self.wavelet_scale = _ScaleModule([1, d * 4, 1, 1], init_scale=0.5)
        self.wavelet_scale1 = _ScaleModule([1, d * 4, 1, 1], init_scale=0.5)
        self.device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        self.d = d
        
        self.pointwise = nn.Conv2d(d, d, kernel_size=1, stride=1, padding=0, bias=False)
        
        self.bn=nn.BatchNorm2d(d)

        

    def to_3d(self, x):
        return rearrange(x, 'b c h w -> b (h w) c')

    def to_4d(self, x, h, w):
        return rearrange(x, 'b (h w) c -> b c h w', h=h, w=w)

    def forward(self, x):
        x1 = x[0]  # ir
        x2 = x[1]  # rgb (the modality that needs repair)
        x1_ll = []
        x2_ll = []
        x1_h = []
        x2_h = []
        while (x2.shape[3] > 64):
            curr_shape = x2.shape
            if (curr_shape[2] % 2 > 0) or (curr_shape[3] % 2 > 0):
                curr_pads = (0, curr_shape[3] % 2, 0, curr_shape[2] % 2)
                x1 = F.pad(x1, curr_pads)
                x2 = F.pad(x2, curr_pads)
            x1 = self.wt_function(x1).view(curr_shape[0], curr_shape[1]*4,curr_shape[2]//2, curr_shape[3]//2)
            x2 = self.wt_function(x2).view(curr_shape[0], curr_shape[1]*4,curr_shape[2]//2, curr_shape[3]//2)
            x1 = self.wavelet_scale(x1).view(curr_shape[0], curr_shape[1],4, curr_shape[2]//2, curr_shape[3]//2)
            x2 = self.wavelet_scale1(x2).view(curr_shape[0], curr_shape[1],4, curr_shape[2]//2, curr_shape[3]//2)
            x1_ll.append(x1[:, :, 0, :, :])
            x2_ll.append(x2[:, :, 0, :, :])
            x1_h.append(x1[:, :, 1:4, :, :])
            x2_h.append(x2[:, :, 1:4, :, :])
            x1 = x1[:, :, 0, :, :]
            x2 = x2[:, :, 0, :, :]
        x1_ll.pop()
        x2_ll.pop()
        b,c,h,w=x1.shape
        x1 = torch.cat([x1.unsqueeze(2), x1_h[-1]], dim=2)
        x2 = torch.cat([x2.unsqueeze(2), x2_h[-1]], dim=2)

        x2 = self.block(x1, x2)
        count=0
        while (x1_h != []):
            if count != 0:
                x2 = self.scale(torch.add(x2_ll.pop(), x2))
            curr_x_tag = torch.cat([x2.unsqueeze(2), x1_h.pop()], dim=2)
            x2 = self.iwt_function(curr_x_tag)
            count += 1
        
        x_tag = x2
        x = x[1]
        x = self.base_scale(self.base_conv(x))
        x = x + x_tag
        x = self.bn(x)
        x = self.pointwise(x)

        return x


